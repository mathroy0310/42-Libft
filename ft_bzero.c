#include"libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*s;

	i = -1;
	ss = (unsigned char *)s;
	while (++i < n)
	{
		ss[i] = 0;
	}
	return (s);
}
